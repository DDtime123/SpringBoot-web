package tacos;

public class UserQuery {
    private String username;
    private int age;
    private int toAge;

    public void setUsername(String username) {
        this.username = username;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getToAge() {
        return toAge;
    }

    public void setToAge(int toAge) {
        this.toAge = toAge;
    }

    public String getUsername(){
        return username;
    }
}
